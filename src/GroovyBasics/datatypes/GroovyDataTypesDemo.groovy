package GroovyBasics.datatypes

class GroovyDataTypesDemo {

        static main(args){

            def byteVariable = 100
            println("Byte value is: "+byteVariable)

            def shortVariable = 1000
            println("Short value is: "+shortVariable)

            def intVariable = 10000
            println("Integer value is: "+intVariable)

            def longVariable = 1000000L
            println("Long value is: "+longVariable)

            def floatVariable = 20.1f
            println("Float value is: "+floatVariable)

            def doubleVariable = 60.33
            println("Double value is: "+doubleVariable)

            def booleanVariable = true
            println("Boolean value is: "+booleanVariable)

            def charVariable = 'b'
            println("Char value is: "+charVariable)
        }

    }
